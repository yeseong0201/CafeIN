package com.example.cafein2;

public class Item {
    private int image;
    private String name;
    private String kcal;
    private String mg;

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getKcal() {
        return kcal;
    }

    public String getMg() {
        return mg;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setKcal(String kcal) {
        this.kcal = kcal;
    }

    public void setMg(String mg) {
        this.mg = mg;
    }
}
